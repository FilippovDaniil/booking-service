package com.booking.entity.enums;

/**
 * Роли пользователей в системе.
 * CLIENT    — обычный клиент, может бронировать квартиры и оставлять отзывы.
 * LANDLORD  — владелец квартир, может создавать объявления и управлять бронями.
 * ADMIN     — администратор, имеет доступ ко всем операциям.
 */
public enum Role {
    CLIENT,
    LANDLORD,
    ADMIN
}
